// Observer Pattern ==>
// allows many objects to subscribe to events...
// that are broadcast by another object
// it's a one-to-many relationship

// in the real world you might have a radio tower
// that sends out a signal then a bunch of receivers
// who listen in at the same time

// this pattern is used all over the place in app development

// like in firebase when your data changes on the server
// all your client apps are subscribed to it,
// automatically updated with the latest data

// like redux store - when data change,
// then all subscribed components are received updated data...

// like Youtube subscribe - when new video uploaded,
// then all send notification to all subscribed user
// or user receive updated data...

// it's a push-based system
